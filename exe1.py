c = float(input("Enter temperature in Celsius :"))  
# calculate fahrenheit  
fahrenheit = (c* 1.8) + 32  
print('%.1f degree Celsius is equal to %.1f degree Fahrenheit' %(c,fahrenheit))  
f = float(input("Enter temperature in Fahrenheit :"))  
# calculate Celsius  
celsius = (f - 32) / 1.8  
print('%.1f degree Fahrenheit is equal to %.1f degree Celsius' %(f,celsius))